public class Variables 
{
    public static void main(String[] args)
    {
        // 1. Declare and initialize a variable for each scenario:
        
        // A friend's name
        String name = "Karthik";
        // Your lucky number
        int lucky = 67;
        // Whether or not you are you a morning person (the value 
        // should be either `true` or `false`)
        boolean isMorningPerson = false;
        // Your favorite snack
        String snack = "Monster Energy Zero Ultra";
        // The price of your favorite snack
        double price = 2.49;
        
        // 2. Print out each variable:
        System.out.println(name);
        System.out.println(lucky);
        System.out.println(isMorningPerson);
        System.out.println(snack);
        System.out.println(price);
    }
}
